/// Periodic table data and utilities.
class ElementData {
  /// Chemical symbol (e.g., 'H', 'He', 'Li')
  final String symbol;

  /// Full element name (e.g., 'Hydrogen', 'Helium', 'Lithium')
  final String name;

  /// Atomic number
  final int atomicNumber;

  /// Atomic mass in atomic mass units (u)
  final double atomicMass;

  /// Element category (e.g., 'noble gas', 'alkali metal')
  final String category;

  /// Period number in the periodic table
  final int period;

  /// Group number in the periodic table
  final int group;

  /// Electron configuration
  final String electronConfiguration;

  /// Electronegativity (Pauling scale)
  final double? electronegativity;

  /// Density at room temperature (g/cm³)
  final double? density;

  /// Melting point in Kelvin
  final double? meltingPoint;

  /// Boiling point in Kelvin
  final double? boilingPoint;

  /// Standard state at room temperature (solid, liquid, gas)
  final String standardState;

  ElementData({
    required this.symbol,
    required this.name,
    required this.atomicNumber,
    required this.atomicMass,
    required this.category,
    required this.period,
    required this.group,
    required this.electronConfiguration,
    this.electronegativity,
    this.density,
    this.meltingPoint,
    this.boilingPoint,
    required this.standardState,
  });
}

