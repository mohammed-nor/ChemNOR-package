/// Utility class for periodic table operations
class PeriodicTable {
  /// Gets an element by its symbol
  static ElementData? getBySymbol(String symbol) {
    return periodicTable[symbol];
  }

  /// Gets an element by its atomic number
  static ElementData? getByAtomicNumber(int atomicNumber) {
    return periodicTable.values.firstWhere((element) => element.atomicNumber == atomicNumber, orElse: () => throw StateError('No element with atomic number $atomicNumber'));
  }

  /// Gets all elements in a specific group
  static List<ElementData> getByGroup(int group) {
    return periodicTable.values.where((element) => element.group == group).toList();
  }

  /// Gets all elements in a specific period
  static List<ElementData> getByPeriod(int period) {
    return periodicTable.values.where((element) => element.period == period).toList();
  }

  /// Gets all elements in a specific category
  static List<ElementData> getByCategory(String category) {
    return periodicTable.values.where((element) => element.category.toLowerCase() == category.toLowerCase()).toList();
  }

  /// Gets elements with atomic mass in the specified range
  static List<ElementData> getByMassRange(double min, double max) {
    return periodicTable.values.where((element) => element.atomicMass >= min && element.atomicMass <= max).toList();
  }

  /// Gets elements with the specified standard state
  static List<ElementData> getByStandardState(String state) {
    return periodicTable.values.where((element) => element.standardState.toLowerCase() == state.toLowerCase()).toList();
  }
}
